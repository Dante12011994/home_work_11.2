def upper(text):
    """
    Весь текст в верхнем регистре
    """
    return text.upper()


def capitalize(text):
    """
    Каждое слово с заглавной буквы
    """
    return text.capitalize()

def lower(text):
    """
    Возвращает все маленькими буквами

    :param text:
    :return:
    """
    return text.lower()
